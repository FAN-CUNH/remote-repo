package basejava.day1.test;

public class operator { // 运算符
    public static void main(String[] args) {
        int a = 5,b = 10;
        System.out.println("a="+a); // 显示：5
        System.out.println("a+b="+(a+b)); // add加法
        System.out.println("b-a="+(b-a)); // sub减法
        System.out.println("a*b="+(a*b)); // mul乘法
        System.out.println("b/a="+(b/a)); // div除法
        System.out.println("b%a="+(b%a)); // mod取余
    }
}
