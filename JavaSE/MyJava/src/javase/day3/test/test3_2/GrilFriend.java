public class GrilFriend {
    private String name;
    private int age;

    public GrilFriend(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public GrilFriend() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
